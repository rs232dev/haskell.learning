module Cap_9.Next_Steps where


-- -------------------------------------------------------------------------- --
-- if/then/else                                                               --
-- -------------------------------------------------------------------------- --    
mySignum x = 
    if x < 0
        then -1
        else if x > 0 
            then 1
            else 0

-- Main> mySignum (-1)
-- -1

-- Main> mySignum 3
-- 1

-- Main> mySignum 0
-- 0

-- -------------------------------------------------------------------------- --
-- if/then/else by Guards                                                     --
-- -------------------------------------------------------------------------- --
mySignum' x
    | x < 0     = -1
    | x > 0     = 1
    | otherwise = 0

-- Main> mySignum' (-9)
-- -1

-- Main> mySignum' 9
-- 1

-- Main> mySignum' 0
-- 0

